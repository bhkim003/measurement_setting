#!/bin/bash
###############################################################################
# run_lm_eval.sh
# ──────────────────────────────────────────────────────────────────────────────
# Llama 3.2 1B 모델로 lm_eval(Language Model Evaluation Harness)을 돌리기 위한
# 원스톱 셸 스크립트입니다.
#
# ★ 사용법
#   chmod +x run_lm_eval.sh
#   ./run_lm_eval.sh
#
# ★ 이 스크립트가 하는 일
#   1) Python 가상환경(venv) 생성
#   2) 필요한 패키지 설치 (transformers, torch, lm-eval 등)
#   3) (선택) 모델 구조 분석 스크립트 실행
#   4) lm_eval 벤치마크 실행
###############################################################################

set -e  # 에러 발생 시 즉시 중단

# ──────────────────────────────────────────────────────────────────────────────
# 0. 기본 설정
# ──────────────────────────────────────────────────────────────────────────────
# Hugging Face 모델 이름 (Llama 3.2 1B)
MODEL_NAME="meta-llama/Llama-3.2-1B"

# lm_eval에서 돌릴 태스크 목록 (쉼표로 구분)
# 아래에서 자세히 설명합니다.
TASKS="hellaswag,arc_easy,winogrande,piqa,boolq"

# GPU 번호 (없으면 cpu로 동작)
DEVICE="cuda:0"

# 배치 크기 — GPU 메모리에 맞게 조절하세요
BATCH_SIZE="auto"

# 결과 저장 디렉토리
OUTPUT_DIR="./lm_eval_results"

# ──────────────────────────────────────────────────────────────────────────────
# 1. Python 가상환경 만들기
# ──────────────────────────────────────────────────────────────────────────────
echo "============================================="
echo "  [1/4] Python 가상환경(venv) 생성 중..."
echo "============================================="

VENV_DIR="./venv_llama_eval"

if [ ! -d "$VENV_DIR" ]; then
    python3 -m venv "$VENV_DIR"
    echo "  → 가상환경이 ${VENV_DIR} 에 생성되었습니다."
else
    echo "  → 이미 가상환경이 존재합니다. 건너뜁니다."
fi

# 가상환경 활성화
# shellcheck disable=SC1091 -- 가상환경 경로가 동적이라 정적 분석 시 파일을 찾을 수 없음
source "${VENV_DIR}/bin/activate"

# ──────────────────────────────────────────────────────────────────────────────
# 2. 패키지 설치
# ──────────────────────────────────────────────────────────────────────────────
echo ""
echo "============================================="
echo "  [2/4] 필요한 Python 패키지 설치 중..."
echo "============================================="

pip install --upgrade pip

# ── 핵심 패키지 설명 ──
# torch            : PyTorch — 딥러닝 프레임워크 (GPU 연산)
# transformers     : Hugging Face Transformers — LLM 모델 로딩/추론
# accelerate       : 대규모 모델을 효율적으로 GPU에 올리는 유틸리티
# lm-eval          : EleutherAI의 lm-evaluation-harness (벤치마크 도구)
# sentencepiece    : 토크나이저가 필요로 하는 서브워드 분할 라이브러리
# protobuf         : 일부 모델 가중치 파일 로딩에 필요

pip install torch transformers accelerate lm-eval sentencepiece protobuf

echo "  → 패키지 설치 완료!"

# ──────────────────────────────────────────────────────────────────────────────
# 3. 모델 구조 분석 스크립트 실행 (선택)
# ──────────────────────────────────────────────────────────────────────────────
echo ""
echo "============================================="
echo "  [3/4] Llama 3.2 1B 모델 구조 분석..."
echo "============================================="

# inspect_model.py를 실행하여 모델의 내부 구조를 출력합니다.
SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
python3 "${SCRIPT_DIR}/inspect_model.py" --model_name "${MODEL_NAME}"

# ──────────────────────────────────────────────────────────────────────────────
# 4. lm_eval 벤치마크 실행
# ──────────────────────────────────────────────────────────────────────────────
echo ""
echo "============================================="
echo "  [4/4] lm_eval 벤치마크 실행 중..."
echo "============================================="
echo "  모델   : ${MODEL_NAME}"
echo "  태스크 : ${TASKS}"
echo "  디바이스: ${DEVICE}"
echo ""

# ── lm_eval CLI 명령어 설명 ──
# --model hf                   : Hugging Face 모델을 사용하겠다는 의미
# --model_args pretrained=...  : 사용할 모델의 이름/경로
# --tasks                      : 실행할 벤치마크 태스크 목록
# --device                     : 사용할 디바이스 (cuda:0, cpu 등)
# --batch_size                 : 배치 크기 (auto=자동, 숫자도 가능)
# --output_path                : 결과를 JSON으로 저장할 경로
# --log_samples                : 각 샘플의 로그를 함께 저장

mkdir -p "${OUTPUT_DIR}"

lm_eval --model hf \
    --model_args "pretrained=${MODEL_NAME}" \
    --tasks "${TASKS}" \
    --device "${DEVICE}" \
    --batch_size "${BATCH_SIZE}" \
    --output_path "${OUTPUT_DIR}" \
    --log_samples

echo ""
echo "============================================="
echo "  ✅ 완료! 결과가 ${OUTPUT_DIR} 에 저장되었습니다."
echo "============================================="
echo ""

# ──────────────────────────────────────────────────────────────────────────────
# ★ 참고: GPU가 없는 경우 (CPU 모드)
# ──────────────────────────────────────────────────────────────────────────────
# DEVICE="cpu" 로 바꾸면 CPU에서도 동작합니다.
# 단, Llama 3.2 1B도 CPU로 돌리면 매우 느립니다.
#
# ★ 참고: Hugging Face 로그인이 필요한 경우
# Llama 3.2는 gated model이므로 Hugging Face 토큰이 필요합니다.
# 아래 명령어로 먼저 로그인하세요:
#   huggingface-cli login
# 또는 환경변수로 설정:
#   export HF_TOKEN="hf_여러분의_토큰"
# ──────────────────────────────────────────────────────────────────────────────
